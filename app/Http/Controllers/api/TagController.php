     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $count = $request->count; 
        $categoryId = $request->category_id;

        $query = Tag::query();

        if (isset($categoryId))
            $query->where('category_id', $categoryId);
        if (isset($count))
            $tags = $query->take($count);
    
        $tags = $query->get();
        return response()->json($tags);
    }

